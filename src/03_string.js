var nome = "AguleCode";


nome.charAt(2); // Retorna o char da posição
nome.charCodeAt(0); // Retorna o char da posoição
nome.concat("Silva"); // Concatena sua string
nome.indexOf("e"); //Retorna a indice da primeirar ocorrência do char
nome.lastIndexOf(); // retorna o índice da ultima ocorrência do char
nome.length(); // retorna o tamanho da string
nome.match("silva"); // retorna um array resultante da busca de um RegExp
nome.replace("silvar", "Medeiros"); // Substitui parte da string por outra
nome.search(); // retorna a posição da string ou RegExp
nome.split(";"); // divide a string com base na expressão regular informada
nome.substring(0,5); //extrai a parte da string desejada
nome.toLocaleLowerCase(); //Gera uma nova sting com letras minuscula
nome.toUpperCase(); //Gera uma nova string com letra maiscula
nome.trim(); // remove os espaços em branco do início e do fim da string
nome.valueOf(); // retorna o valor da string